package com.base.spring.forms;

public class EnumForm {
	private String id;
	private String eenum;
	private String txt;
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getEenum() {
		return eenum;
	}
	public void setEenum(String eenum) {
		this.eenum = eenum;
	}
	public String getTxt() {
		return txt;
	}
	public void setTxt(String txt) {
		this.txt = txt;
	}
}
